def average():  # объявляем функцию без параметра, которая выводит среднее значение чисел в листе
    arr = []  # переменная, которая ссылается на пустой лист
    count_operation = 1  # переменная, которая ссылается на значение 1 (отражает число операций)
    while count_operation <= 10:  # цикл: пока количество операций меньше или равно 10
        num = random.randint(1, 1000)  # переменная, которая ссылается на значение рандомного числа
        arr.append(num)  # метод, который добавляет значение переменной в конец листа
        count_operation += 1  # + 1 к значению переменной

    print(sum(arr) / len(arr))  # вывод в кансоль


