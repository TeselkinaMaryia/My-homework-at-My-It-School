def is_year_leap(year):  # объявляем функцию с одним параметром, которая будет возвращать True если год високосный
    if year % 4 == 0 and year % 100 != 0 or year % 400 == 0:
        # условие: усли год делиться на 4 без остатка и не делиться на 100 без остатка или делиться на 400 без остатка
        return True  # функция, которая возвращает  True
    else:  # если условие не выполняется
        return False  # функция, которая возвращает  False


