def square(length):
    # объявляем функцию с одним параметром, которая будет высчитывать периметр, площать и диагональ квадрата
    return 4 * length, length ** 2, length * math.sqrt(2)
    # функция, которая возвращает значения периметра, площади и диагонали квадрата


