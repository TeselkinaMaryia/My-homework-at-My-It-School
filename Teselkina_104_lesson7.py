def is_prime(digit):  # объявляем функцию с одним параметром, которая выясняет является ли число простым
    count_divider = 0  # количество делителей для числа
    if 0 <= digit < 2:  # условие: если число = 2
        print(f'число {digit} не является ни простым ни составным')
    elif digit == 2:  # условие: если число = 2
        return True  # функция, которая возвращает True

    for divider in range(2, digit):  # цикл: для каждого элемента в последовательности от 2 до дисла
        if digit % divider == 0:  # если число делиться на элемент в последовательности
            count_divider += 1  # добавляем 1 к значению переменной

    if count_divider > 0:  # условие: если значение переменной больше 0
        return False  # функция, которая возвращает False
    elif count_divider == 0:  # если значение переменной = 0
        return True  # функция, которая возвращает True


